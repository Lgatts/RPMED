package users;

public class Doctor {

    public String id;
    public String email;
    public String name;
    public String fone;
    public String cpf;

    public Doctor(String id, String email, String name, String fone, String cpf) {
        this.id = id;
        this.email = email;
        this.name = name;
        this.fone = fone;
        this.cpf = cpf;
    }    
}
